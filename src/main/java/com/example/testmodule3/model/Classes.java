package com.example.testmodule3.model;

public class Classes {
    private int id;
    private String nameC;

    public Classes(int id, String nameC) {
        this.id = id;
        this.nameC = nameC;
    }

    public Classes() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNameC() {
        return nameC;
    }

    public void setNameC(String nameC) {
        this.nameC = nameC;
    }
}
